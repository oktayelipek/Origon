// Origon UI token build pipeline.
// Reads tokens/source/*.json (DTCG format) and emits language-specific outputs
// for React (TypeScript + CSS variables), Flutter (Dart), and Swift (SwiftUI).
//
// Not using Style Dictionary yet — a lightweight custom generator keeps the
// pipeline transparent while we only have primitive/literal tokens. Swap in
// Style Dictionary once we introduce token references (semantic layer).

import { readFileSync, writeFileSync, mkdirSync, existsSync, readdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SOURCE_DIR = path.join(__dirname, 'source');
const ROOT = path.dirname(__dirname);

const BANNER = `// GENERATED by tokens/build.config.mjs. Do not edit by hand.\n// Regenerate with: pnpm build:tokens\n`;

// Default build: Kripto brand, Dark theme. Additional brands/themes are emitted
// as separate variant files (see emitVariants below).
const DEFAULT_BRAND = 'kripto';
const DEFAULT_THEME = 'dark';

// Files that are always merged into the primary token tree.
const CORE_FILES = ['colors.json', 'typography.json', 'sizes.json'];

// -----------------------------------------------------------------------------
// Load and merge DTCG source files for a given brand × theme combination.
// -----------------------------------------------------------------------------
function loadTokens(brand = DEFAULT_BRAND, theme = DEFAULT_THEME) {
  const files = [
    ...CORE_FILES,
    `theme.${theme}.json`,
    `semantic.${brand}-${theme}.json`,
  ];
  const merged = {};
  for (const file of files) {
    const p = path.join(SOURCE_DIR, file);
    if (!existsSync(p)) continue;
    deepMerge(merged, JSON.parse(readFileSync(p, 'utf8')));
  }
  return merged;
}

function deepMerge(target, source) {
  for (const [key, value] of Object.entries(source)) {
    if (key.startsWith('$')) continue;
    if (value && typeof value === 'object' && !Array.isArray(value) && !('$value' in value)) {
      target[key] = target[key] ?? {};
      deepMerge(target[key], value);
    } else {
      target[key] = value;
    }
  }
}

function walk(node, visit, pathParts = []) {
  if (!node || typeof node !== 'object') return;
  if ('$value' in node) {
    visit(pathParts, node);
    return;
  }
  for (const [key, child] of Object.entries(node)) {
    if (key.startsWith('$')) continue;
    walk(child, visit, [...pathParts, key]);
  }
}

// -----------------------------------------------------------------------------
// React (TypeScript + CSS variables)
// -----------------------------------------------------------------------------
function emitReact(tokens) {
  const outDir = path.join(ROOT, 'packages/tokens-react/src/generated');
  mkdirSync(outDir, { recursive: true });
  const cssLines = [':root {'];
  const exports = [];

  // Colors
  if (tokens.color) {
    const tree = extractLeafValues(tokens.color);
    writeFileSync(
      path.join(outDir, 'colors.ts'),
      BANNER +
        `export const colors = ${serializeJs(tree)} as const;\n\nexport type Colors = typeof colors;\nexport type ColorStep = 50 | 100 | 200 | 300 | 400 | 500 | 600 | 700 | 800 | 900;\n`,
    );
    exports.push("export * from './colors';");
    walk(tokens.color, (parts, node) => {
      cssLines.push(`  --color-${['', ...parts].filter(Boolean).map(kebab).join('-')}: ${node.$value};`);
    });
  }

  // Spacing
  if (tokens.spacing) {
    const flat = flattenDimensions(tokens.spacing);
    writeFileSync(
      path.join(outDir, 'spacing.ts'),
      BANNER + `export const spacing = ${serializeJs(flat, { valueTransform: (v) => `${v}` })} as const;\n`,
    );
    exports.push("export * from './spacing';");
    for (const [k, v] of Object.entries(flat)) {
      cssLines.push(`  --spacing-${kebab(k)}: ${v}px;`);
    }
  }

  // Radius
  if (tokens.radius) {
    const flat = flattenDimensions(tokens.radius);
    writeFileSync(
      path.join(outDir, 'radius.ts'),
      BANNER + `export const radius = ${serializeJs(flat)} as const;\n`,
    );
    exports.push("export * from './radius';");
    for (const [k, v] of Object.entries(flat)) {
      cssLines.push(`  --radius-${kebab(k)}: ${v}px;`);
    }
  }

  // Typography
  if (tokens.text || tokens.font) {
    const font = tokens.font ? extractLeafValues(tokens.font) : {};
    const text = tokens.text ? extractLeafValues(tokens.text) : {};
    const body = `${BANNER}export const font = ${serializeJs(font)} as const;\n\nexport const text = ${serializeJs(text)} as const;\n`;
    writeFileSync(path.join(outDir, 'typography.ts'), body);
    exports.push("export * from './typography';");

    if (font.family) {
      for (const [k, v] of Object.entries(font.family)) {
        cssLines.push(`  --font-family-${kebab(k)}: ${JSON.stringify(v)};`);
      }
    }
    if (font.weight) {
      for (const [k, v] of Object.entries(font.weight)) {
        cssLines.push(`  --font-weight-${kebab(k)}: ${v};`);
      }
    }
    for (const [scale, dims] of Object.entries(text)) {
      for (const [dimKey, val] of Object.entries(dims)) {
        cssLines.push(`  --text-${kebab(scale)}-${kebab(dimKey)}: ${val}${dimKey.endsWith('Size') || dimKey.endsWith('Height') ? 'px' : ''};`);
      }
    }
  }

  // Semantic colors
  if (tokens.semantic) {
    const tree = extractLeafValues(tokens.semantic);
    writeFileSync(
      path.join(outDir, 'semantic.ts'),
      BANNER + `export const semantic = ${serializeJs(tree)} as const;\n`,
    );
    exports.push("export * from './semantic';");
    walk(tokens.semantic, (parts, node) => {
      cssLines.push(`  --semantic-${parts.map(kebab).join('-')}: ${node.$value};`);
    });
  }

  // Sizes
  if (tokens.size) {
    const flat = flattenDimensions(tokens.size);
    writeFileSync(
      path.join(outDir, 'sizes.ts'),
      BANNER + `export const size = ${serializeJs(flat)} as const;\n`,
    );
    exports.push("export * from './sizes';");
    for (const [k, v] of Object.entries(flat)) {
      cssLines.push(`  --size-${kebab(k)}: ${v}px;`);
    }
  }

  // Width / Height (from Themes)
  if (tokens.width) {
    const flat = flattenDimensions(tokens.width);
    writeFileSync(
      path.join(outDir, 'width.ts'),
      BANNER + `export const width = ${serializeJs(flat)} as const;\n`,
    );
    exports.push("export * from './width';");
    for (const [k, v] of Object.entries(flat)) cssLines.push(`  --width-${kebab(k)}: ${v}px;`);
  }
  if (tokens.height) {
    const flat = flattenDimensions(tokens.height);
    writeFileSync(
      path.join(outDir, 'height.ts'),
      BANNER + `export const height = ${serializeJs(flat)} as const;\n`,
    );
    exports.push("export * from './height';");
    for (const [k, v] of Object.entries(flat)) cssLines.push(`  --height-${kebab(k)}: ${v}px;`);
  }

  // Blur (dimensions)
  if (tokens.blur) {
    const flat = flattenDimensions(tokens.blur);
    writeFileSync(
      path.join(outDir, 'blur.ts'),
      BANNER + `export const blur = ${serializeJs(flat)} as const;\n`,
    );
    exports.push("export * from './blur';");
    for (const [k, v] of Object.entries(flat)) cssLines.push(`  --blur-${kebab(k)}: ${v}px;`);
  }

  // Shadow (composite — offsetX, offsetY, blur, spread, color)
  if (tokens.shadow) {
    const shadows = extractLeafValues(tokens.shadow); // e.g. { xl: { offsetX, offsetY, blur, spread, color } }
    // Emit as pre-baked CSS box-shadow strings + TS object with the shorthand.
    const cssShadows = {};
    for (const [key, parts] of Object.entries(shadows)) {
      const s = `${parts.offsetX ?? 0}px ${parts.offsetY ?? 0}px ${parts.blur ?? 0}px ${parts.spread ?? 0}px ${parts.color ?? 'rgba(0,0,0,0)'}`;
      cssShadows[key] = s;
      cssLines.push(`  --shadow-${kebab(key)}: ${s};`);
    }
    writeFileSync(
      path.join(outDir, 'shadow.ts'),
      BANNER + `export const shadow = ${serializeJs(cssShadows)} as const;\n\nexport const shadowParts = ${serializeJs(shadows)} as const;\n`,
    );
    exports.push("export * from './shadow';");
  }

  cssLines.push('}\n');
  writeFileSync(path.join(outDir, 'tokens.css'), BANNER + cssLines.join('\n'));
  // Preserve the legacy import path used by @origon/tokens-react/css.
  writeFileSync(path.join(outDir, 'colors.css'), BANNER + cssLines.join('\n'));

  writeFileSync(path.join(outDir, 'index.ts'), BANNER + exports.join('\n') + '\n');

  return { files: readdirSync(outDir), leaves: countAllLeaves(tokens) };
}

