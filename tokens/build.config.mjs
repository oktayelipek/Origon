const BRANDS = ['kripto', 'hisse', 'global'];
const THEMES = ['dark', 'light'];

function emitFlutterThemes() {
  const outDir = path.join(ROOT, 'packages/tokens-flutter/lib/generated');
  mkdirSync(outDir, { recursive: true });

  // Discover semantic structure from the default mode. All 6 modes share the
  // same shape (only values differ).
  const seed = loadTokens(DEFAULT_BRAND, DEFAULT_THEME).semantic ?? {};
  const groupNames = Object.keys(seed);

  // Group -> ordered list of leaf field names.
  const groupFields = {};
  for (const g of groupNames) {
    const fields = [];
    for (const [k, v] of Object.entries(seed[g])) {
      if (isLeaf(v)) fields.push(k);
    }
    groupFields[g] = fields;
  }

  const lines = [
    BANNER,
    "import 'package:flutter/painting.dart';",
    '',
    '/// Origon UI semantic color tokens across all 6 brand × theme modes.',
    '/// Access via `OrigonTheme.of(context).semantic.button.primary`.',
    '',
  ];

  // Emit one class per semantic group.
  for (const g of groupNames) {
    const cls = `OrigonSemantic${cap(g)}`;
    lines.push(`class ${cls} {`);
    for (const f of groupFields[g]) lines.push(`  final Color ${dartId(f)};`);
    lines.push('');
    lines.push(`  const ${cls}({`);
    for (const f of groupFields[g]) lines.push(`    required this.${dartId(f)},`);
    lines.push('  });');
    lines.push('}');
    lines.push('');
  }

  // Aggregate semantic root class.
  lines.push('class OrigonSemanticColors {');
  for (const g of groupNames) lines.push(`  final OrigonSemantic${cap(g)} ${g};`);
  lines.push('');
  lines.push('  const OrigonSemanticColors({');
  for (const g of groupNames) lines.push(`    required this.${g},`);
  lines.push('  });');
  lines.push('}');
  lines.push('');

  // ThemeData wrapper.
  lines.push('enum OrigonBrand { kripto, hisse, global }');
  lines.push('enum OrigonThemeMode { dark, light }');
  lines.push('');
  lines.push('class OrigonThemeData {');
  lines.push('  final OrigonBrand brand;');
  lines.push('  final OrigonThemeMode mode;');
  lines.push('  final OrigonSemanticColors semantic;');
  lines.push('  const OrigonThemeData({required this.brand, required this.mode, required this.semantic});');
  lines.push('}');
  lines.push('');

  // Emit constant OrigonSemanticColors for each brand×theme.
  for (const brand of BRANDS) {
    for (const theme of THEMES) {
      const t = loadTokens(brand, theme);
      const s = t.semantic ?? {};
      lines.push(`const OrigonSemanticColors _${brand}${cap(theme)}Semantic = OrigonSemanticColors(`);
      for (const g of groupNames) {
        lines.push(`  ${g}: OrigonSemantic${cap(g)}(`);
        for (const f of groupFields[g]) {
          const val = s[g]?.[f]?.$value;
          const color = val ? flutterColor(val) : 'Color(0x00000000)';
          lines.push(`    ${dartId(f)}: ${color},`);
        }
        lines.push('  ),');
      }
      lines.push(');');
      lines.push('');
    }
  }

  // Public map keyed by brand×theme.
  lines.push('class OrigonThemes {');
  for (const brand of BRANDS) {
    for (const theme of THEMES) {
      const name = `${brand}${cap(theme)}`;
      lines.push(`  static const OrigonThemeData ${name} = OrigonThemeData(`);
      lines.push(`    brand: OrigonBrand.${brand},`);
      lines.push(`    mode: OrigonThemeMode.${theme},`);
      lines.push(`    semantic: _${brand}${cap(theme)}Semantic,`);
      lines.push('  );');
    }
  }
  lines.push('');
  lines.push('  static OrigonThemeData resolve(OrigonBrand brand, OrigonThemeMode mode) {');
  lines.push('    switch (brand) {');
  for (const brand of BRANDS) {
    lines.push(`      case OrigonBrand.${brand}:`);
    lines.push(`        return mode == OrigonThemeMode.dark ? ${brand}Dark : ${brand}Light;`);
  }
  lines.push('    }');
  lines.push('  }');
  lines.push('}');
  lines.push('');

  writeFileSync(path.join(outDir, 'origon_themes.dart'), lines.join('\n'));

  return { modes: BRANDS.length * THEMES.length, groups: groupNames.length };
}

function emitSwiftThemes() {
  const outDir = path.join(ROOT, 'packages/tokens-swift/Sources/OrigonTokens/generated');
  mkdirSync(outDir, { recursive: true });

  const seed = loadTokens(DEFAULT_BRAND, DEFAULT_THEME).semantic ?? {};
  const groupNames = Object.keys(seed);
  const groupFields = {};
  for (const g of groupNames) {
    groupFields[g] = Object.keys(seed[g]).filter((k) => isLeaf(seed[g][k]));
  }

  const lines = [BANNER, 'import SwiftUI', '', '/// Origon UI semantic color tokens across all 6 brand × theme modes.', ''];

  // Struct per group.
  for (const g of groupNames) {
    lines.push(`public struct OrigonSemantic${cap(g)} {`);
    for (const f of groupFields[g]) lines.push(`    public let ${swiftId(f)}: Color`);
    lines.push('');
    lines.push(`    public init(${groupFields[g].map((f) => `${swiftId(f)}: Color`).join(', ')}) {`);
    for (const f of groupFields[g]) lines.push(`        self.${swiftId(f)} = ${swiftId(f)}`);
    lines.push('    }');
    lines.push('}');
    lines.push('');
  }

  lines.push('public struct OrigonSemanticColors {');
  for (const g of groupNames) lines.push(`    public let ${g}: OrigonSemantic${cap(g)}`);
  lines.push('');
  lines.push(`    public init(${groupNames.map((g) => `${g}: OrigonSemantic${cap(g)}`).join(', ')}) {`);
  for (const g of groupNames) lines.push(`        self.${g} = ${g}`);
  lines.push('    }');
  lines.push('}');
  lines.push('');

  lines.push('public enum OrigonBrand: String, CaseIterable { case kripto, hisse, global }');
  lines.push('public enum OrigonThemeMode: String, CaseIterable { case dark, light }');
  lines.push('');
  lines.push('public struct OrigonThemeData {');
  lines.push('    public let brand: OrigonBrand');
  lines.push('    public let mode: OrigonThemeMode');
  lines.push('    public let semantic: OrigonSemanticColors');
  lines.push('');
  lines.push('    public init(brand: OrigonBrand, mode: OrigonThemeMode, semantic: OrigonSemanticColors) {');
  lines.push('        self.brand = brand; self.mode = mode; self.semantic = semantic');
  lines.push('    }');
  lines.push('}');
  lines.push('');

  // Per-mode constants inside enum.
  lines.push('public enum OrigonThemes {');
  for (const brand of BRANDS) {
    for (const theme of THEMES) {
      const t = loadTokens(brand, theme);
      const s = t.semantic ?? {};
      const name = `${brand}${cap(theme)}`;
      lines.push(`    public static let ${name} = OrigonThemeData(`);
      lines.push(`        brand: .${brand}, mode: .${theme},`);
      lines.push(`        semantic: OrigonSemanticColors(`);
      for (const [gi, g] of groupNames.entries()) {
        const args = groupFields[g].map((f) => {
          const val = s[g]?.[f]?.$value;
          const hex = val ? '0x' + val.replace('#', '').slice(0, 6).toUpperCase() : '0x000000';
          return `${swiftId(f)}: Color(hex: ${hex})`;
        }).join(', ');
        const suffix = gi === groupNames.length - 1 ? '' : ',';
        lines.push(`            ${g}: OrigonSemantic${cap(g)}(${args})${suffix}`);
      }
      lines.push('        )');
      lines.push('    )');
    }
  }
  lines.push('');
  lines.push('    public static func resolve(_ brand: OrigonBrand, _ mode: OrigonThemeMode) -> OrigonThemeData {');
  lines.push('        switch (brand, mode) {');
  for (const brand of BRANDS) {
    for (const theme of THEMES) {
      lines.push(`        case (.${brand}, .${theme}): return ${brand}${cap(theme)}`);
    }
  }
  lines.push('        }');
  lines.push('    }');
  lines.push('}');
  lines.push('');

  writeFileSync(path.join(outDir, 'OrigonThemes.swift'), lines.join('\n'));

  return { modes: BRANDS.length * THEMES.length, groups: groupNames.length };
}

function emitReactThemes() {
  const outDir = path.join(ROOT, 'packages/tokens-react/src/generated/themes');
  mkdirSync(outDir, { recursive: true });

  const cssLines = [BANNER, '/* Scoped tokens for every brand × theme combination.', ' * Apply the appropriate data-* attributes to the root element:', ' *   <html data-brand="kripto" data-theme="dark">                 */', ''];

  const variantsIndex = [];

  for (const brand of BRANDS) {
    for (const theme of THEMES) {
      const t = loadTokens(brand, theme);
      const selector = `[data-brand="${brand}"][data-theme="${theme}"]`;
      const nested = brand === DEFAULT_BRAND && theme === DEFAULT_THEME
        ? [':root, ' + selector]
        : [selector];

      // CSS variables — walk full tree.
      cssLines.push(`${nested.join(', ')} {`);
      if (t.color) walk(t.color, (parts, node) => cssLines.push(`  --color-${['', ...parts].filter(Boolean).map(kebab).join('-')}: ${node.$value};`));
      if (t.semantic) walk(t.semantic, (parts, node) => cssLines.push(`  --semantic-${parts.map(kebab).join('-')}: ${node.$value};`));
      if (t.spacing) {
        for (const [k, v] of Object.entries(flattenDimensions(t.spacing))) cssLines.push(`  --spacing-${kebab(k)}: ${v}px;`);
      }
      if (t.radius) {
        for (const [k, v] of Object.entries(flattenDimensions(t.radius))) cssLines.push(`  --radius-${kebab(k)}: ${v}px;`);
      }
      cssLines.push('}', '');

      // TS variant module — exports resolved values as a nested object.
      const tsBody = [
        BANNER,
        `// Brand: ${brand}, Theme: ${theme}`,
        '',
        `export const tokens = {`,
        `  color: ${t.color ? serializeJs(extractLeafValues(t.color)) : '{}'},`,
        `  semantic: ${t.semantic ? serializeJs(extractLeafValues(t.semantic)) : '{}'},`,
        `  spacing: ${t.spacing ? serializeJs(flattenDimensions(t.spacing)) : '{}'},`,
        `  radius: ${t.radius ? serializeJs(flattenDimensions(t.radius)) : '{}'},`,
        `} as const;`,
        '',
      ].join('\n');
      const variantFile = `${brand}-${theme}.ts`;
      writeFileSync(path.join(outDir, variantFile), tsBody);
      variantsIndex.push({ brand, theme, file: variantFile });
    }
  }

  writeFileSync(path.join(outDir, 'themes.css'), cssLines.join('\n'));

  // Central index — provides types + a `themes` map + a `applyTheme` helper.
  const idxLines = [
    BANNER,
    ...variantsIndex.map(
      ({ brand, theme, file }) => `import { tokens as ${brand}${cap(theme)} } from './${file.replace(/\.ts$/, '')}';`,
    ),
    '',
    `export type Brand = ${BRANDS.map((b) => JSON.stringify(b)).join(' | ')};`,
    `export type Theme = ${THEMES.map((t) => JSON.stringify(t)).join(' | ')};`,
    '',
    'export const themes = {',
    ...variantsIndex.map(({ brand, theme }) => `  '${brand}-${theme}': ${brand}${cap(theme)},`),
    '} as const;',
    '',
    'export type ThemeKey = keyof typeof themes;',
    '',
    'export function getTokens(brand: Brand, theme: Theme) {',
    "  return themes[`${brand}-${theme}` as ThemeKey];",
    '}',
    '',
    '/** Applies a brand/theme by setting data-* attributes on document.documentElement. */',
    'export function applyTheme(brand: Brand, theme: Theme) {',
    "  if (typeof document === 'undefined') return;",
    "  document.documentElement.setAttribute('data-brand', brand);",
    "  document.documentElement.setAttribute('data-theme', theme);",
    '}',
    '',
  ];
  writeFileSync(path.join(outDir, 'index.ts'), idxLines.join('\n'));

  return { variants: variantsIndex.length };
}

