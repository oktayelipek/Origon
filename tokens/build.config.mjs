// Flutter
// -----------------------------------------------------------------------------
function emitFlutter(tokens) {
  const outDir = path.join(ROOT, 'packages/tokens-flutter/lib/generated');
  mkdirSync(outDir, { recursive: true });
  const emittedFiles = [];

  // Colors — keeps existing shape.
  if (tokens.color) {
    const lines = [
      BANNER,
      "import 'package:flutter/painting.dart';",
      '',
      '/// Origon UI primitive + brand color tokens.',
      'class OrigonColors {',
      '  OrigonColors._();',
      '',
    ];
    const swatches = [];
    for (const [category, children] of Object.entries(tokens.color)) {
      if (isSwatch(children)) {
        swatches.push({ name: category, children });
      } else if (isLeaf(children)) {
        lines.push(`  static const Color ${category} = ${flutterColor(children.$value)};`);
      } else {
        for (const [sub, subChildren] of Object.entries(children)) {
          if (isSwatch(subChildren)) {
            swatches.push({ name: `${category}${cap(sub)}`, children: subChildren });
          } else if (isLeaf(subChildren)) {
            lines.push(`  static const Color ${sub} = ${flutterColor(subChildren.$value)};`);
          }
        }
      }
    }
    lines.push('');
    for (const { name, children } of swatches) {
      lines.push(`  static const OrigonSwatch ${name} = OrigonSwatch(`);
      lines.push(
        '    ' +
          [50, 100, 200, 300, 400, 500, 600, 700, 800, 900]
            .map((s) => `s${s}: ${flutterColor(children[String(s)].$value)}`)
            .join(', '),
      );
      lines.push('  );');
    }
    lines.push('}');
    lines.push('');
    lines.push('class OrigonSwatch {');
    lines.push('  final Color s50, s100, s200, s300, s400, s500, s600, s700, s800, s900;');
    lines.push('');
    lines.push('  const OrigonSwatch({');
    lines.push('    required this.s50, required this.s100, required this.s200,');
    lines.push('    required this.s300, required this.s400, required this.s500,');
    lines.push('    required this.s600, required this.s700, required this.s800,');
    lines.push('    required this.s900,');
    lines.push('  });');
    lines.push('');
    lines.push('  Color operator [](int step) {');
    lines.push('    switch (step) {');
    for (const s of [50, 100, 200, 300, 400, 500, 600, 700, 800, 900]) {
      lines.push(`      case ${s}:  return s${s};`);
    }
    lines.push("      default: throw ArgumentError('Step must be 50 or 100..900, got: \\$step');");
    lines.push('    }');
    lines.push('  }');
    lines.push('}');
    lines.push('');
    writeFileSync(path.join(outDir, 'origon_colors.dart'), lines.join('\n'));
    emittedFiles.push('origon_colors.dart');
  }

  if (tokens.spacing) {
    emittedFiles.push(
      emitDartDoubles(outDir, 'origon_spacing.dart', 'OrigonSpacing', flattenDimensions(tokens.spacing)),
    );
  }
  if (tokens.radius) {
    emittedFiles.push(
      emitDartDoubles(outDir, 'origon_radius.dart', 'OrigonRadius', flattenDimensions(tokens.radius)),
    );
  }
  if (tokens.font || tokens.text) {
    emittedFiles.push(emitDartTypography(outDir, tokens));
  }
  if (tokens.semantic) {
    emittedFiles.push(emitDartSemantic(outDir, tokens.semantic));
  }
  if (tokens.size) {
    emittedFiles.push(
      emitDartDoubles(outDir, 'origon_size.dart', 'OrigonSize', flattenDimensions(tokens.size)),
    );
  }
  if (tokens.width) {
    emittedFiles.push(
      emitDartDoubles(outDir, 'origon_width.dart', 'OrigonWidth', flattenDimensions(tokens.width)),
    );
  }
  if (tokens.height) {
    emittedFiles.push(
      emitDartDoubles(outDir, 'origon_height.dart', 'OrigonHeight', flattenDimensions(tokens.height)),
    );
  }
  if (tokens.blur) {
    emittedFiles.push(
      emitDartDoubles(outDir, 'origon_blur.dart', 'OrigonBlur', flattenDimensions(tokens.blur)),
    );
  }
  if (tokens.shadow) {
    emittedFiles.push(emitDartShadow(outDir, tokens.shadow));
  }

  return { files: emittedFiles, leaves: countAllLeaves(tokens) };
}

function emitDartShadow(outDir, shadow) {
  const shadows = extractLeafValues(shadow);
  const lines = [
    BANNER,
    "import 'package:flutter/painting.dart';",
    '',
    '/// Origon UI shadow tokens (Flutter BoxShadow parts).',
    'class OrigonShadow {',
    '  OrigonShadow._();',
    '',
  ];
  for (const [key, parts] of Object.entries(shadows)) {
    const dartKey = /^[a-zA-Z_]/.test(key) ? key : `d${key}`;
    const color = String(parts.color ?? '#0000001a').replace('#', '').toUpperCase();
    // Parse 6/8-char hex → ARGB (default alpha ff if 6 chars).
    const hex = color.length === 8
      ? color.slice(6, 8) + color.slice(0, 6)
      : 'FF' + color;
    lines.push(`  static const BoxShadow ${dartKey} = BoxShadow(`);
    lines.push(`    color: Color(0x${hex}),`);
    lines.push(`    offset: Offset(${parts.offsetX ?? 0}.0, ${parts.offsetY ?? 0}.0),`);
    lines.push(`    blurRadius: ${parts.blur ?? 0}.0,`);
    lines.push(`    spreadRadius: ${parts.spread ?? 0}.0,`);
    lines.push('  );');
  }
  lines.push('}', '');
  writeFileSync(path.join(outDir, 'origon_shadow.dart'), lines.join('\n'));
  return 'origon_shadow.dart';
}

function emitDartSemantic(outDir, semantic) {
  const lines = [
    BANNER,
    "import 'package:flutter/painting.dart';",
    '',
    '/// Origon UI semantic color tokens — reference these in product code, not the primitives.',
    'class OrigonSemantic {',
    '  OrigonSemantic._();',
    '',
  ];
  for (const [group, children] of Object.entries(semantic)) {
    const groupClass = cap(group);
    lines.push(`  static const _Origon${groupClass} ${group} = _Origon${groupClass}();`);
  }
  lines.push('}');
  lines.push('');
  for (const [group, children] of Object.entries(semantic)) {
    const groupClass = cap(group);
    lines.push(`class _Origon${groupClass} {`);
    lines.push(`  const _Origon${groupClass}();`);
    for (const [k, v] of Object.entries(children)) {
      if (isLeaf(v)) {
        lines.push(`  Color get ${dartId(k)} => ${flutterColor(v.$value)};`);
      }
    }
    lines.push('}');
    lines.push('');
  }
  writeFileSync(path.join(outDir, 'origon_semantic.dart'), lines.join('\n'));
  return 'origon_semantic.dart';
}

function emitDartDoubles(outDir, filename, className, entries) {
  const lines = [BANNER, '/// Origon UI dimension tokens.', `class ${className} {`, `  ${className}._();`, ''];
  for (const [k, v] of Object.entries(entries)) {
    lines.push(`  static const double ${dartId(k)} = ${v}.0;`);
  }
  lines.push('}', '');
  writeFileSync(path.join(outDir, filename), lines.join('\n'));
  return filename;
}

function emitDartTypography(outDir, tokens) {
  const font = tokens.font ? extractLeafValues(tokens.font) : {};
  const text = tokens.text ? extractLeafValues(tokens.text) : {};
  const lines = [
    BANNER,
    "import 'package:flutter/painting.dart';",
    '',
    '/// Origon UI typography tokens.',
    'class OrigonFont {',
    '  OrigonFont._();',
    '',
  ];
  for (const [k, v] of Object.entries(font.family ?? {})) {
    lines.push(`  static const String ${dartId(k)} = ${JSON.stringify(v)};`);
  }
  lines.push('');
  for (const [k, v] of Object.entries(font.weight ?? {})) {
    lines.push(`  static const FontWeight ${dartId(k)} = FontWeight.w${v};`);
  }
  lines.push('}');
  lines.push('');
  lines.push('class OrigonTextStyles {');
  lines.push('  OrigonTextStyles._();');
  lines.push('');
  for (const [scale, dims] of Object.entries(text)) {
    const size = dims.fontSize;
    const lh = dims.lineHeight;
    lines.push(
      `  static const TextStyle ${dartId(scale)} = TextStyle(fontSize: ${size}.0, height: ${(lh / size).toFixed(4)});`,
    );
  }
  lines.push('}', '');
  writeFileSync(path.join(outDir, 'origon_typography.dart'), lines.join('\n'));
  return 'origon_typography.dart';
}

