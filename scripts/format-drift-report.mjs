#!/usr/bin/env node
// scripts/format-drift-report.mjs
//
// Reads the current git working tree diff for tokens/source/ and produces a
// markdown report summarising what changed (color hexes with inline swatches,
// added/removed tokens, dimension shifts). Consumed by the figma-drift
// workflow to fill the PR body.
//
// Prints to stdout. Exits 0 if there is nothing to report (clean tree).

import { execSync } from 'node:child_process';
import { readFileSync, existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.dirname(__dirname);
const SOURCE_DIR = path.join(ROOT, 'tokens/source');

// Get list of changed JSON files under tokens/source (staged, unstaged, or
// untracked).
const changed = execSync('git status --porcelain tokens/source/', { cwd: ROOT, encoding: 'utf8' })
  .split('\n')
  .filter(Boolean)
  .map((l) => l.trim().split(/\s+/).pop())
  .filter((p) => p.endsWith('.json'));

if (!changed.length) { console.log('No drift detected.'); process.exit(0); }

const sections = [];
sections.push(`## 🎨 Figma token drift detected`);
sections.push('');
sections.push(`${changed.length} source file(s) changed. Review the diff and merge to accept, or close to reject.`);
sections.push('');

let totalAdded = 0, totalChanged = 0, totalRemoved = 0;

for (const file of changed) {
  const abs = path.join(ROOT, file);
  const short = path.relative(SOURCE_DIR, abs);
  let oldContent;
  try { oldContent = execSync(`git show HEAD:${file}`, { cwd: ROOT, encoding: 'utf8' }); }
  catch { oldContent = null; }
  const newContent = existsSync(abs) ? readFileSync(abs, 'utf8') : null;

  if (!oldContent && newContent) {
    sections.push(`### 📄 \`${short}\` — new file`);
    sections.push('');
    continue;
  }
  if (oldContent && !newContent) {
    sections.push(`### 🗑 \`${short}\` — deleted`);
    sections.push('');
    continue;
  }

  const oldFlat = flatten(safeParse(oldContent));
  const newFlat = flatten(safeParse(newContent));
  const oldKeys = new Set(Object.keys(oldFlat));
  const newKeys = new Set(Object.keys(newFlat));

  const added = [...newKeys].filter((k) => !oldKeys.has(k));
  const removed = [...oldKeys].filter((k) => !newKeys.has(k));
  const changedKeys = [...newKeys].filter((k) => oldKeys.has(k) && oldFlat[k] !== newFlat[k]);

  if (!added.length && !removed.length && !changedKeys.length) continue;

  totalAdded += added.length;
  totalChanged += changedKeys.length;
  totalRemoved += removed.length;

  sections.push(`### \`${short}\``);
  sections.push('');
  if (changedKeys.length) {
    sections.push(`**Changed (${changedKeys.length}):**`);
    sections.push('');
    sections.push('| Token | Before | After |');
    sections.push('|---|---|---|');
    for (const k of changedKeys.slice(0, 30)) {
      sections.push(`| \`${k}\` | ${fmt(oldFlat[k])} | ${fmt(newFlat[k])} |`);
    }
    if (changedKeys.length > 30) sections.push(`| … | +${changedKeys.length - 30} more | |`);
    sections.push('');
  }
  if (added.length) {
    sections.push(`**Added (${added.length}):** ${added.slice(0, 10).map((k) => `\`${k}\``).join(', ')}${added.length > 10 ? ` +${added.length - 10} more` : ''}`);
    sections.push('');
  }
  if (removed.length) {
    sections.push(`**Removed (${removed.length}):** ${removed.slice(0, 10).map((k) => `\`${k}\``).join(', ')}${removed.length > 10 ? ` +${removed.length - 10} more` : ''}`);
    sections.push('');
  }
}

sections.push('---');
sections.push(`**Summary**: ${totalChanged} changed · ${totalAdded} added · ${totalRemoved} removed.`);
sections.push('');
sections.push('_Auto-generated by `scripts/format-drift-report.mjs` on the nightly Figma drift workflow._');

console.log(sections.join('\n'));

// -----------------------------------------------------------------------------
function safeParse(s) { try { return JSON.parse(s); } catch { return {}; } }

function flatten(node, prefix = '') {
  const out = {};
  if (node && typeof node === 'object') {
    if ('$value' in node) { out[prefix] = node.$value; return out; }
    for (const [k, v] of Object.entries(node)) {
      if (k.startsWith('$')) continue;
      Object.assign(out, flatten(v, prefix ? `${prefix}.${k}` : k));
    }
  }
  return out;
}

function fmt(v) {
  if (typeof v === 'string' && /^#[0-9a-f]{6,8}$/i.test(v)) {
    return `\`${v}\` <sub>█</sub>`; // GitHub markdown doesn't render color swatches inline reliably; fall back to code + block.
  }
  if (typeof v === 'string') return `\`${v}\``;
  return String(v);
}
